# Kategori selanjutnya adalah memisah dan menggabung string. 
# Anda dapat menggunakan metode ini untuk tujuan tersebut dengan menggunakan dua metode berikut.

print("".join(['Muhamad ','Nabil ','Firdaus ', '!']))

# Perhatikan bahwa pada sintaks awal sebelum ".join()" Ada double quotes di sana. 
# Double quotes ini bermaksud agar Anda menentukan delimiter pada setiap kata/nilai yang ingin Anda gabungkan. 
# Pada kode di atas, delimiter-nya adalah whitespace atau spasi.

# Contoh Lain

print("Muhamad ".join(['Nabil ','Firdaus ', '!']))

# Pada kode di atas, Anda memasukkan delimeter "Muhamad" sehingga output-nya adalah "Nabil Muhamad Firdaus Muhamad".

