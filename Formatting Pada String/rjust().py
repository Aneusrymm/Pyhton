# Metode rjust() berguna untuk merapikan pencetakan teks. 
# Dengan metode rjust() ini, Anda dapat membuat teks menjadi rata kanan sehingga tampilannya lebih rapi.

print('Muhamad'.rjust(20))

"""
Output:
            Muhamad Nabil Firdaus

"""

# Cntoh lain Jika white space nya ingin di tambahkan hal lain tetapi hanya untuk satu karakter saja
print('Muhamad '.rjust(20, '!'))

"""
Output:
!!!!!!!!!!!!Muhamad
"""