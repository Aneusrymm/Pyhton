"""
Metode center() menjadikan teks rata tengah.
Metode ini akan menambahkan whitespace di sebelah kiri dan kanan secara default. 
Anda juga bisa mengganti whitespace tersebut dengan karakter lain.

"""

print('Nabil'.center(7, '='))

"""
Output:
=Nabil=

"""

# Jika angka 7 di ubah dalam center()

print('Nabil'.center(20, '='))

"""
Output:
=======Nabil========
"""